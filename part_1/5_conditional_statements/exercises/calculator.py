num_1 = int(input("Number 1: "))
num_2 = int(input("Number 2: "))
operation = input("Operation: ")

if operation == "add":
    print(f"{num_1} + {num_2} = {num_1 + num_2}")

if operation == "subtract":
    print(f"{num_1} - {num_2} = {num_1 - num_2}")

if operation == "multiply":
    print(f"{num_1} * {num_2} = {num_1 * num_2}")